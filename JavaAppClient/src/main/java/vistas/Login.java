    public boolean validarAcceso(String usuario, String contraseña) {
        try {
            // Realizar la solicitud HTTP GET a la API para obtener los operadores
            String apiUrl = "http://192.168.0.109/WS/listarOperadores.php";
            HttpClient client = HttpClientBuilder.create().build();
            HttpGet request = new HttpGet(apiUrl);
            HttpResponse response = client.execute(request);

            // Obtener la respuesta como texto JSON
            HttpEntity entity = response.getEntity();
            String jsonResponse = EntityUtils.toString(entity);

            // Convertir la respuesta JSON en un arreglo de objetos JSON
            JSONArray operadores = new JSONArray(jsonResponse);

            // Validar el acceso comparando los datos del usuario y la contraseña
            for (int i = 0; i < operadores.length(); i++) {
                JSONObject operador = operadores.getJSONObject(i);
                String operadorUsuario = operador.getString("usuario");
                String operadorContraseña = operador.getString("pass");

                if (usuario.equals(operadorUsuario) && contraseña.equals(operadorContraseña)) {
                    return true; // Acceso válido
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        return false; // Acceso inválido
    }

